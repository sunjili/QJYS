    public static File createTmpFile(Context context) throws IOException{
        File dir = null;
        if(TextUtils.equals(Environment.getExternalStorageState(), Environment.MEDIA_MOUNTED)) {
            dir = Environment.getExternalStoragePublicDirectory(Environment.DIRECTORY_DCIM);
            if (!dir.exists()) {
                dir = Environment.getExternalStoragePublicDirectory(Environment.DIRECTORY_DCIM + "/Camera");
                if (!dir.exists()) {
                    dir = getCacheDirectory(context, true);
                }
            }
        }else{
            dir = getCacheDirectory(context, true);
        }
        return File.createTempFile(JPEG_FILE_PREFIX, JPEG_FILE_SUFFIX, dir);
    }
    
    public static File createImageFile(Context context) throws IOException{
    	File dir = null;
    	if(TextUtils.equals(Environment.getExternalStorageState(), Environment.MEDIA_MOUNTED)) {
    		dir = Environment.getExternalStoragePublicDirectory(Environment.DIRECTORY_DCIM);
    		if (!dir.exists()) {
    			dir = Environment.getExternalStoragePublicDirectory(Environment.DIRECTORY_DCIM + "/Camera");
    			if (!dir.exists()) {
    				dir = getCacheDirectory(context, true);
    			}
    		}
    	}else{
    		dir = getCacheDirectory(context, true);
    	}
    	Date date = new Date(System.currentTimeMillis());  
	    SimpleDateFormat dateFormat = new SimpleDateFormat("MMdd_HHmmss");  
		String strFileName = JPEG_FILE_PREFIX+dateFormat.format(date);
    	return File.createTempFile(strFileName, JPEG_FILE_SUFFIX, dir);
    }

	public static String saveImage(Context context, Bitmap bitmap) {
		FileOutputStream fos = null;
		try {
			File imageFile = FileUtils.createImageFile(context);
			fos = new FileOutputStream(imageFile);
			bitmap.compress(CompressFormat.JPEG, 100, fos);
			fos.flush();
			return imageFile.getAbsolutePath();
		} catch (FileNotFoundException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} finally {
			try {
				bitmap.recycle();
				fos.close();
			} catch (Exception e) {
				e.printStackTrace();
			}
		}
		return null;
	}

